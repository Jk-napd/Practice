	//i.e. element 4 so element does not exist and other case be lower_bound == 'n'
	//As we discussed while implementing Lower Bound that it will 'n' if element is 
	//not present in the given array
	//But what if the element is present?
	//BS         [1, 2, 4, 4, 4, 5], x = 4
	//So, the lower_bound = 2nd index and upper_bound = 5th index
	//So first_occurence = lower_bound i.e. 2nd index and
	//last_occurence = upper_bound - 1 i.e. 4th index 
	//------------------------------------------------------------------------------------

	int n = a.size();
	int lb = lowerBound(a, x);
	if(lb == n || a[lb] != x) return {-1, -1};
